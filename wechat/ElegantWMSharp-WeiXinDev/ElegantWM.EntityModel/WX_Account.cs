namespace ElegantWM.EntityModel
{
    public class WX_Account : Entity
    {
        public WX_Account() { }

		/// <summary>
		/// 原始ID
		/// </summary>
		public string OpenId { get; set; }
		
		/// <summary>
		/// AppId
		/// </summary>
		public string AppId { get; set; }
		
		/// <summary>
		/// AppSecret
		/// </summary>
		public string AppSecret { get; set; }
		
		/// <summary>
		/// 是否认证
		/// </summary>
		public int IsAuthed { get; set; }
		
		/// <summary>
		/// 是否接入
		/// </summary>
		public int IsAccessed { get; set; }
		
		/// <summary>
		/// Token
		/// </summary>
		public string Token { get; set; }
		
		/// <summary>
		/// 账号名称
		/// </summary>
		public string AccountName { get; set; }

        /// <summary>
        /// 微信登录邮箱
        /// </summary>
        public string LoginEmail { get; set; }

        /// <summary>
        /// 微信登录密码
        /// </summary>
        public string LoginPwd { get; set; }

		/// <summary>
		/// 欢迎词
		/// </summary>
		public string WelcomeWord { get; set; }

        /// <summary>
        /// 帐套ID
        /// </summary>
        public Guid TntId { get; set; }
         
    }
}
