package org.hy.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@SuppressWarnings("serial")
@Entity
@Table(name = "SCFX_TYBMB1", schema = "dbo")
public class Tybmb {
	
	@Id
	@Column(name = "guid", unique = true, nullable = false, length = 32)
	private String guid;
	@Column(name = "FCODE")
	private String code;
	@Column(name = "FSCOPE")
	private String scope;
	@Column(name = "FPARENTCODE")
	private String parentCode;
	@Column(name = "FCONTENT")
	private String content;
	@Column(name = "FDESCRIPTION")
	private String description;
	@Column(name = "FSequence")
	private int sequence;
	@Column(name = "FDELETEFLAG")
	private String deleteFlag;
	@Column(name = "FDELETETIME")
	private String deleteTime;
	@Column(name = "CreatePerson")
	private String createPerson;
	@Column(name = "CreateTime")
	private String createTime;
	@Column(name = "UpdatePerson")
	private String updatePerson;
	@Column(name = "UpdateTime")
	private String updateTime;
	
	
	public String getGuid() {
		return guid;
	}
	public void setGuid(String guid) {
		this.guid = guid;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getScope() {
		return scope;
	}
	public void setScope(String scope) {
		this.scope = scope;
	}
	public String getParentCode() {
		return parentCode;
	}
	public void setParentCode(String parentCode) {
		this.parentCode = parentCode;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public String getDeleteFlag() {
		return deleteFlag;
	}
	public void setDeleteFlag(String deleteFlag) {
		this.deleteFlag = deleteFlag;
	}
	public String getDeleteTime() {
		return deleteTime;
	}
	public void setDeleteTime(String deleteTime) {
		this.deleteTime = deleteTime;
	}
	public String getCreatePerson() {
		return createPerson;
	}
	public void setCreatePerson(String createPerson) {
		this.createPerson = createPerson;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getUpdatePerson() {
		return updatePerson;
	}
	public void setUpdatePerson(String updatePerson) {
		this.updatePerson = updatePerson;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	
	
	
}
